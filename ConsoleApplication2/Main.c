#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <windows.h>

#define MAX_R 30

long dp[MAX_R + 1][3][2];

int main() {
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);
    int p;
    printf("Введіть число розрядів p: ");
    scanf("%d", &p);

    // Ініціалізація базових випадків
    dp[1][1][0] = 1; // Один розряд: цифра 5
    dp[1][1][1] = 1; // Один розряд: цифра 9

    // Заповнення таблиці dp
    for (int i = 2; i <= p; ++i) {
        // Якщо остання цифра 5 (0)
        dp[i][1][0] = dp[i - 1][1][1] + dp[i - 1][2][1]; // додати нову цифру 5 до числа, яке закінчується на одну або дві 9
        dp[i][2][0] = dp[i - 1][1][0]; // додати другу цифру 5 до числа, яке закінчується на одну 5

        // Якщо остання цифра 9 (1)
        dp[i][1][1] = dp[i - 1][1][0] + dp[i - 1][2][0]; // додати нову цифру 9 до числа, яке закінчується на одну або дві 5
        dp[i][2][1] = dp[i - 1][1][1]; // додати другу цифру 9 до числа, яке закінчується на одну 9
    }

    // Рахуємо кількість чисел довжиною p
    long result = dp[p][1][0] + dp[p][2][0] + dp[p][1][1] + dp[p][2][1];
    printf("Кількість чисел із %d розрядів: %lld\n", p, result);

    return 0;
}
